package ch14.book.s1406;

public class Calculator {
	public static int staticMethod(int x, int y) {   // 정적 메소드
		return x + y;							     // 정적 메소드
	}												 // 정적 메소드
	
	public int instanceMethod(int x , int y) {       // 인스턴스 메소드
		return x + y;								 // 인스턴스 메소드
	} 												 // 인스턴스 메소드
}
