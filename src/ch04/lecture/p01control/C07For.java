package ch04.lecture.p01control;

public class C07For {
	public static void main(String[] args) {
		for (int i = 1; i <=5 ; i++) {
			System.out.println(i);
		}
		
		System.out.println("----");
		
		for (int j = 5; j <=10 ; j++) {
			System.out.println(j);
		}
		
		System.out.println("----");
		
		
		for (int k = 5; k >=1 ; k--) {
			System.out.println(k);
		}
		
		System.out.println("----");
		
		for (int l = 10; l >=5 ; l--) {
			System.out.println(l);
		}
		
		
	}
}
